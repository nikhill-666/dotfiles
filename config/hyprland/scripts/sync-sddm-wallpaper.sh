#!/bin/bash

# Script to sync wallpaper between swww/hyprlock and SDDM
# This script needs to be run with sudo for copying to system directories

WALLPAPER_CACHE_DIR="$HOME/.config/hypr/cache"
WALLPAPER_FILE="$WALLPAPER_CACHE_DIR/current_wallpaper"
SDDM_THEME_DIR="/usr/share/sddm/themes/silent"
SDDM_CONFIG="/usr/share/sddm/themes/silent/theme.conf"

# Function to get current wallpaper from cache file
get_current_wallpaper() {
    # When running with sudo, use the original user's home directory
    if [ -n "$SUDO_USER" ]; then
        local user_home=$(getent passwd "$SUDO_USER" | cut -d: -f6)
        local user_cache_file="$user_home/.config/hypr/cache/current_wallpaper"
        if [ -f "$user_cache_file" ]; then
            cat "$user_cache_file"
            return
        fi
    fi
    
    # Fallback: use local cache file or swww
    if [ -f "$WALLPAPER_FILE" ]; then
        cat "$WALLPAPER_FILE"
    else
        # Try to get from swww as current user
        swww query 2>/dev/null | grep "image:" | sed 's/.*image: //' | xargs
    fi
}

# Function to copy wallpaper to SDDM theme directory
copy_wallpaper_to_sddm() {
    local wallpaper_path="$1"
    
    if [ -z "$wallpaper_path" ] || [ ! -f "$wallpaper_path" ]; then
        echo "Error: Invalid wallpaper path: $wallpaper_path"
        return 1
    fi
    
    # Get wallpaper filename
    local wallpaper_name=$(basename "$wallpaper_path")
    local sddm_wallpaper_path="$SDDM_THEME_DIR/backgrounds/$wallpaper_name"
    
    echo "Copying wallpaper to SDDM: $wallpaper_path -> $sddm_wallpaper_path"
    
    # Copy wallpaper to SDDM backgrounds directory
    cp "$wallpaper_path" "$sddm_wallpaper_path"
    if [ $? -eq 0 ]; then
        echo "Wallpaper copied successfully"
        echo "$wallpaper_name" > "$WALLPAPER_CACHE_DIR/sddm_wallpaper_name"
        return 0
    else
        echo "Error: Failed to copy wallpaper"
        return 1
    fi
}

