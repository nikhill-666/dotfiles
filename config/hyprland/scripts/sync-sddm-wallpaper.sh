# Function to sync current wallpaper to SDDM
sync_wallpaper() {
    echo "Syncing wallpaper to SDDM..."
    
    # Get current wallpaper
    local current_wallpaper=$(get_current_wallpaper)
    
    if [ -z "$current_wallpaper" ]; then
        echo "Error: No current wallpaper found"
        return 1
    fi
    
    echo "Current wallpaper: $current_wallpaper"
    
    # Copy wallpaper to SDDM
    if copy_wallpaper_to_sddm "$current_wallpaper"; then
        local wallpaper_name=$(basename "$current_wallpaper")
        update_sddm_config "$wallpaper_name"
        echo "Wallpaper sync completed successfully"
        return 0
    else
        echo "Error: Failed to sync wallpaper"
        return 1
    fi
}

# Function to monitor wallpaper changes
monitor_wallpaper_changes() {
    echo "Monitoring wallpaper changes for SDDM sync..."
    
    # Create cache directory
    mkdir -p "$WALLPAPER_CACHE_DIR"
    
    # Initial sync
    sync_wallpaper
    
    # Monitor for changes
    while true; do
        current=$(get_current_wallpaper)
        stored=""
        if [ -f "$WALLPAPER_FILE" ]; then
            stored=$(cat "$WALLPAPER_FILE")
        fi
        
        if [ "$current" != "$stored" ]; then
            echo "Wallpaper changed, syncing to SDDM..."
            sync_wallpaper
        fi
        
        sleep 2
    done
}

# Main execution
case "${1:-sync}" in
    "sync")
        sync_wallpaper
        ;;
    "monitor")
        monitor_wallpaper_changes
        ;;
    *)
        echo "Usage: $0 [sync|monitor]"
        echo "  sync    - Sync current wallpaper to SDDM (default)"
        echo "  monitor - Monitor and auto-sync wallpaper changes"
        exit 1
        ;;
